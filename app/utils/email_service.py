logger = logging.getLogger(__name__)

# –í—Ä–µ–º–µ–Ω–Ω–æ–µ —Ö—Ä–∞–Ω–∏–ª–∏—â–µ –∫–æ–¥–æ–≤ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è
# –í –ø—Ä–æ–¥–∞–∫—à–µ–Ω–µ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ Redis –∏–ª–∏ –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö
verification_codes = {}

def generate_verification_code():
    """
    –ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç 6-–∑–Ω–∞—á–Ω—ã–π –∫–æ–¥ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è.
    
    Returns:
        str: 6-–∑–Ω–∞—á–Ω—ã–π –∫–æ–¥
    """
    return ''.join([str(secrets.randbelow(10)) for _ in range(6)])

def store_verification_code(email, code):
    """
    –°–æ—Ö—Ä–∞–Ω—è–µ—Ç –∫–æ–¥ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è —Å –≤—Ä–µ–º–µ–Ω–µ–º –∏—Å—Ç–µ—á–µ–Ω–∏—è.
    
    Args:
        email (str): Email –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        code (str): –ö–æ–¥ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è
    """
    expiry_time = time.time() + current_app.config.get('EMAIL_VERIFICATION_CODE_EXPIRY', 900)
    verification_codes[email.lower()] = {
        'code': code,
        'expires_at': expiry_time
    }
    logger.info(f"–ö–æ–¥ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è —Å–æ—Ö—Ä–∞–Ω—ë–Ω –¥–ª—è {email}")

def get_verification_code(email):
    """
    –ü–æ–ª—É—á–∞–µ—Ç —Å–æ—Ö—Ä–∞–Ω—ë–Ω–Ω—ã–π –∫–æ–¥ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è.
    
    Args:
        email (str): Email –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        
    Returns:
        dict or None: –î–∞–Ω–Ω—ã–µ –∫–æ–¥–∞ –∏–ª–∏ None –µ—Å–ª–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω
    """
    return verification_codes.get(email.lower())

def delete_verification_code(email):
    """
    –£–¥–∞–ª—è–µ—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–Ω—ã–π –∫–æ–¥ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è.
    
    Args:
        email (str): Email –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    """
    if email.lower() in verification_codes:
        del verification_codes[email.lower()]
        logger.info(f"–ö–æ–¥ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è —É–¥–∞–ª—ë–Ω –¥–ª—è {email}")

def is_code_expired(email):
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –∏—Å—Ç—ë–∫ –ª–∏ –∫–æ–¥ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è.
    
    Args:
        email (str): Email –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        
    Returns:
        bool: True –µ—Å–ª–∏ –∫–æ–¥ –∏—Å—Ç—ë–∫ –∏–ª–∏ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
    """
    code_data = get_verification_code(email)
    if not code_data:
        return True
    return time.time() > code_data['expires_at']

